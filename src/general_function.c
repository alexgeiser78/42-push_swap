#include"push_swap.h"

int	nbrlen(long int n)
{
	int	i;

	i = 1;
	if (n < 0)
	{
		n = n * -1;
		i++;
	}
	while (n > 9)
	{
		n = n / 10;
	i++;
	}
	return (i);
}
